/*
Will init the grid to :
1 2 3 4
2 3 4 1
3 4 1 2
4 1 2 3
*/
int	**init_bf_1_of_576(int **grid)
{
	grid[0][0] = 1;
	grid[0][1] = 2;
	grid[0][2] = 3;
	grid[0][3] = 4;
	grid[1][0] = 2;
	grid[1][1] = 3;
	grid[1][2] = 4;
	grid[1][3] = 1;
	grid[2][0] = 3;
	grid[2][1] = 4;
	grid[2][2] = 1;
	grid[2][3] = 2;
	grid[3][0] = 4;
	grid[3][1] = 1;
	grid[3][2] = 2;
	grid[3][3] = 3;
}
